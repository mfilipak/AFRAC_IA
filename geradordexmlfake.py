def create_xml(Nome_Produto):
    """
    Cria xml com Dados de Produtos Errados para Treino da Maquina de Aprendizado
    Parameters
    ----------
    Nome_Produto: string
        Parte do Nome do Produto que deseja Criar
    """

    cod_modelo_documento_fiscal = '65'
    natOp = 'Venda de Mercadorias'
    finNFe = 1
    nNF = get_prox_nnf()
    uf_loja = 'RJ'

    items_data_ = inventar_produto(Nome_Produto)

    # --- XML ---
    xml_dict = dict()

    # cabeçalho
    xml_dict['NFe'] = {}
    xml_dict['NFe']['infNFe'] = []

    ## -- IDE
    ide_dict = {}
    ide_dict['ide'] = {}
    ide_dict['ide']['mod'] = cod_modelo_documento_fiscal
    ide_dict['ide']['natOp'] = natOp
    ide_dict['ide']['finNFe'] = finNFe
    ide_dict['ide']['nNF'] = nNF
    ide_dict['ide']['idDest'] = '1'
    ide_dict['ide']['indFinal'] = '1'
    ide_dict['ide']['DescontarICMSDesonerado'] = '2'

    xml_dict['NFe']['infNFe'].append(ide_dict)
    # -- FIM IDE

    ## -- DET
    # iterando sobre cada item para prod e imposto
    for i, items_data in items_data_.iterrows():
        # det
        det_dict = {}
        det_dict['det'] = {}

        # det/prod
        det_dict['det']['prod'] = {}
        det_dict['det']['prod']['cProd'] = xstr(items_data['alu'])
        det_dict['det']['prod']['cEAN'] = xstr(
            items_data['scan_upc']).zfill(13)

        det_dict['det']['prod']['xProd'] = xstr(
            items_data['item_description1'])
        det_dict['det']['prod']['NCM'] = xstr(
            items_data['NCM']).replace('.', '')
        det_dict['det']['prod']['CEST'] = xstr(
            items_data['CEST']).replace('.', '').replace('nan', '')

        # codigo beneficio fiscal
        if xstr(items_data['beneficio_fiscal']) is not None:
            det_dict['det']['prod']['cBenef'] = xstr(items_data['beneficio_fiscal'])

        det_dict['det']['prod']['CFOP'] = xstr(items_data['cfop_out'])
        det_dict['det']['prod']['uCom'] = xstr(items_data['unidade'])
        det_dict['det']['prod']['qCom'] = xstr(items_data['quantity'])
        det_dict['det']['prod']['vUnCom'] = xstr(items_data['original_price'])

        det_dict['det']['prod']['vProd'] = xstr('{0:.2f}'.format(
            float(items_data['original_price']) * float(items_data['quantity'])))
        if xstr(items_data['scan_upc']) != '0':
            det_dict['det']['prod']['cEANTrib'] = xstr(items_data['scan_upc']).zfill(13)
        else:
            det_dict['det']['prod']['cEANTrib'] = xstr(items_data['invn_item_uid']).zfill(13)
        det_dict['det']['prod']['uTrib'] = xstr(items_data['unidade'])
        det_dict['det']['prod']['qTrib'] = xstr(items_data['quantity'])
        det_dict['det']['prod']['vUnTrib'] = xstr(items_data['original_price'])

        # desconto
        # desconto aplicado no item, seja como porcentagem, valor ou modificação no preço da unidade
        desconto_item = float(items_data['total_discount_amount'])

        det_dict['det']['prod']['tpDesc'] = '$'
        det_dict['det']['prod']['vDesc'] = '{0:.2f}'.format(desconto_item)

        # frete
        det_dict['det']['prod']['frete'] = xstr(items_data['frete'])

        # det/imposto
        det_dict['det']['imposto'] = {}
        total_desconto_item = 0

        # det/imposto/icms
        det_dict['det']['imposto']['ICMS'] = {}
        # codigo CST para tag do ICMS
        cst_csosn = str(items_data['cst/csosn_icms'])
