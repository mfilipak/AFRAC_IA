            det_dict['det']['imposto']['ICMS']['ICMS00'] = {}
            det_dict['det']['imposto']['ICMS']['ICMS00']['orig'] = xstr(items_data['origem_cst/csosn'])
            det_dict['det']['imposto']['ICMS']['ICMS00']['CST'] = xstr(items_data['cst/csosn_icms'])
